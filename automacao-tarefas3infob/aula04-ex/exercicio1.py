'''
Exercício 1: crie um programa que recebe como entrada dois números reais.
O programa deve imprimir as quatro operações matemáticas entre esses dois valores (soma, subtração, multiplicação e divisão).
'''

print('Insira o primeiro valor: ') 
num1 = int(input())

print('Insira o segundo valor: ')
num2 = int(input())

print(f'Soma: {num1 + num2}\n')
print(f'Subtração: {num1 - num2}\n')
print(f'Multiplicação: {num1 * num2}\n')
print(f'Divisão: {num1 / num2}\n')
